uint16_t freq = 0;	      // frequenza massima per lampeggiare un led

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	//printf("Interrupt on pin (%d).\n", GPIO_Pin);

	if (GPIO_Pin == GPIO_PIN_4)
	{
    /* Keypad interrupt */
    /**
     * Here you need to read REG_KEY_DATA_1 (status of the column of the keypad) and REG_KEY_DATA_2 (status of the row of the keypad).
     * The addresses of the register are defined in the SX1509_Registers.h file.
     * If you don't read the registers, the interrupt will be NOT triggered again.
     * For an example of how to read/write the registers, see the Init_SX1509 function in the SX1509_Utils.h file.
     */
	 led_on = false;
	 uint8_t MemAddSize = 1;
	 uint8_t column = 0;
	 uint8_t row = 0;
	 uint8_t size = 1;
	 HAL_I2C_Mem_Read(&hi2c1, SX1509_I2C_ADDR2 << 1, REG_KEY_DATA_1, MemAddSize, &column, size, I2C_TIMEOUT);
	 HAL_I2C_Mem_Read(&hi2c1, SX1509_I2C_ADDR2 << 1, REG_KEY_DATA_2, MemAddSize, &row, size, I2C_TIMEOUT);

	 int colPos = GetPosZero(column);  //uint8_t unsigned-> non gestisce il -1
	 int rowPos = GetPosZero(row);
	    if (colPos < 0 || rowPos < 0) {
	         printf("Invalid value, unable to determine the key.\n");
	      } else {
	          char keyPressed = keypadLayout[rowPos][colPos];
	          printf("Key Pressed: %c\n", keyPressed );
	          if (keyPressed >= '0' && keyPressed <= '9'){ // i char sono continui in ascii
	             freq = freq*10 + (keyPressed-'0'); // shift a sinistra + valore, ma in decimale, conversione char a int
	          }
	          else if (keyPressed == '#'){
	        	  led_on = true;
	        	  Blink(freq);
	              freq = 0;
	          }
	          else if (keyPressed == '*'){
	        	  printf("The frequency has been resetted \n");
	        	  freq = 0;
	          }
	          else { printf("The button pressed has no function\n");}
	          printf("Frequency: %d\n", freq);
	      }
	}

}
